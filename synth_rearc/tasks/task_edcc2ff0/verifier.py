from synth_rearc.core import *


def verify_edcc2ff0(I: Grid) -> Grid:
    x0 = crop(I, astuple(SEVEN, ZERO), astuple(add(TEN, THREE), TEN))
    x1 = objects(x0, T, F, T)
    x2 = index(I, astuple(ONE, ZERO))
    x3 = index(I, astuple(THREE, ZERO))
    x4 = index(I, astuple(FIVE, ZERO))
    x5 = colorfilter(x1, x2)
    x6 = colorfilter(x1, x3)
    x7 = colorfilter(x1, x4)
    x8 = combine(combine(x5, x6), x7)
    x9 = merge(x8)
    x10 = mostcolor(x0)
    x11 = canvas(x10, shape(x0))
    x12 = paint(x11, x9)
    x13 = size(x5)
    x14 = size(x6)
    x15 = size(x7)
    x16 = interval(ZERO, x13, ONE)
    x17 = interval(ZERO, x14, ONE)
    x18 = interval(ZERO, x15, ONE)
    x19 = apply(lbind(astuple, ONE), x16)
    x20 = apply(lbind(astuple, THREE), x17)
    x21 = apply(lbind(astuple, FIVE), x18)
    x22 = canvas(ZERO, astuple(SEVEN, TEN))
    x23 = fill(x22, x2, x19)
    x24 = fill(x23, x3, x20)
    x25 = fill(x24, x4, x21)
    x26 = vconcat(x25, x12)
    return x26
